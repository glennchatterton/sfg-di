package guru.springframework.sfgdi.config;

import guru.springframework.sfgdi.datasources.FakeDataSource;
import guru.springframework.sfgdi.datasources.FakeJmsBroker;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.PropertySource;
import org.springframework.context.annotation.PropertySources;
import org.springframework.context.support.PropertySourcesPlaceholderConfigurer;

@Configuration
@PropertySources({
        @PropertySource("classpath:datasource.properties"),
        //@PropertySource("classpath:jms.properties")
})
public class PropertyConfig {

    @Value("${guru.username}")
    public String dbUser;

    @Value("${guru.password}")
    public String dbPassword;

    @Value("${guru.connectionString}")
    public String dbConnectionString;

    @Value("${guru.jms.username}")
    String jmsUsername;

    @Value("${guru.jms.password}")
    String jmsPassword;

    @Value("${guru.jms.url}")
    String jmsUrl;

    //this value comes from the default application.properies.
    @Value("${guru.default.config}")
    String appDefault;

    @Bean
    public FakeDataSource fakeDataSource() {
        FakeDataSource fakeDataSource = new FakeDataSource();
        fakeDataSource.setUser(dbUser);
        fakeDataSource.setPassword(dbPassword);
        fakeDataSource.setUrl(dbConnectionString);
        fakeDataSource.setAppDefauklt(appDefault);
        return fakeDataSource;
    }

    @Bean
    public FakeJmsBroker fakeJmsBroker(){
        FakeJmsBroker jmsBroker = new FakeJmsBroker();
        jmsBroker.setUsername(jmsUsername);
        jmsBroker.setPassword(jmsPassword);
        jmsBroker.setUrl(jmsUrl);
        return jmsBroker;
    }

    @Bean
    public static PropertySourcesPlaceholderConfigurer properties() {
        PropertySourcesPlaceholderConfigurer propertySourcesPlaceholderConfigurer = new PropertySourcesPlaceholderConfigurer();
        return propertySourcesPlaceholderConfigurer;
    }
}
